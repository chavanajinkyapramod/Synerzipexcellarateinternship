/*the = operator*/

let x = 10;
alert(x);

/*The += assignment operator adds a value to a variable.  it is same as x = x+5*/
let x = 10;
x += 5;
alert(x);
/*The -= assignment operator subtracts a value from a variable. it is same as x = x-5 */
let x = 10;
x -= 5;
alert(x);
/*The *= assignment operator multiplies a variable.*/
let x = 10;
x *= 5;
alert(x);
/*The /= assignment divides a variable.*/
let x = 10;
x /= 5;
alert(x);

/*The %= assignment operator assigns a remainder to a variable.*/


let x = 10;
x %= 5;
alert(x);